class PriceDropdown {
    Integer base_fare;
    Integer fees;

    public PriceDropdown() {
    }

    public Integer getBase_fare() {
        return base_fare;
    }

    public void setBase_fare(Integer base_fare) {
        this.base_fare = base_fare;
    }

    public Integer getFees() {
        return fees;
    }

    public void setFees(Integer fees) {
        this.fees = fees;
    }
}
